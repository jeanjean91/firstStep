Les CGV (conditions générales de vente), qu'est-ce que c'est ?
Les conditions générales de vente constituent la base de votre négociation commerciale et de vos relations commerciales. Lorsque vous vous lancez dans une activité de vente de produits ou de prestations de service en B to B (Business to Business) ou B to C (Business to Customer), vous devez informer vos clients de vos conditions générales de vente. Ces conditions écrites permettent de sécuriser vos relations commerciales. Les clauses peuvent être différentes selon que vos produits ou services concernent des consommateurs particuliers ou des professionnels. 

Pourquoi ai-je besoin de créer des CGV (Conditions générales de vente) ?
Le fait de créer des CGV vous permet de définir clairement vos conditions commerciales et d'anticiper toute question, ou litige éventuel avec vos clients, qu'ils soient professionnels ou particuliers. Indiquer vos conditions de vente, le barème des prix unitaires, les réductions de prix et les conditions de règlement sécurisent la relation commerciale en indiquant au client tout ce qu'il doit savoir sur votre prestation, sur vos obligations, et sur ses propres engagements.

De plus, vous devez faire figurer dans vos CGV ou vos mentions légales des mentions obligatoires depuis le RGPD. Toutes nos CGV vous permettent de vous conformer avec cette obligation. 

Y a-t-il des mentions spécifiques pour les consommateurs particuliers ?
Si vos clients sont des consommateurs particuliers, vos CGV doivent prévoir certaines mentions obligatoires. Il s'agit des mentions suivantes :

la garantie légale de conformité ;
la garantie relative aux défauts de la chose vendue ;
les conditions de la garantie commerciale lorsqu'elle est proposée ;
le service après-vente ;
le droit de rétractation de 14 jours lorsqu'il s'applique.
Vous devez communiquer ces informations à vos clients particuliers avant tout contrat (exemple : sur le verso du devis).  Ce sera à vous, professionnel, de prouver que vous avez effectivement et correctement informé votre client, sous peine d’être condamné pour pratique trompeuse ou agressive (sanction pénale possible de 2 ans de prison et amende à partir de 37 500 euros selon le cas)... 

Nos modèles de CGV tiennent compte de ces contraintes, et en les utilisant, vous sécurisez vos relations commerciales.

CGV : produits en ligne pour les professionnels
Vous avez créé votre site de vente de biens ou de produits en ligne pour des professionnels. Pour des négociations commerciales sécurisées, il est nécessaire de formaliser par écrit vos conditions générales de vente. Utilisez un document CGV entre professionnels. Il contient non seulement toutes les mentions obligatoires mais aussi toutes celles recommandées entre professionnels.

CGV : produits en ligne pour les particuliers
Vous avez créé un site de vente de biens ou de produits sur internet à destination des particuliers. Les conditions générales de vente (CGV) pour les produits que vous commercialisez en ligne sont obligatoires. Utilisez notre modèle de CGV de produits en ligne, pour vous permettre de réaliser cette formalité, en toute sécurité juridique.

CGV : prestations de services en ligne pour les professionnels
Vous avez créé un site de vente de prestations de services sur internet, à destination des professionnels. N’oubliez pas les obligations spécifiques à votre charge en tant que prestataire de services : avant tout contrat et avant l'exécution de la prestation de services, vous devez communiquer à votre client une liste précise d’informations. Cette liste permet au client de connaître les caractéristiques essentielles du service. Utilisez notre modèle de CGV de prestations de services en ligne, pour vous permettre de réaliser cette obligation, en toute sécurité juridique.

CGV : prestations de service en ligne pour les particuliers
Vous créez un site de vente de prestations de services sur internet à destination des particuliers. Vous devez penser à rédiger vos conditions générales de vente (CGV) pour les prestations que vous commercialisez en ligne. Les CGV sont obligatoires. Utilisez notre modèle de CGV de prestations de services en ligne pour les particuliers, pour vous permettre de réaliser cette formalité, en toute sécurité juridique.

